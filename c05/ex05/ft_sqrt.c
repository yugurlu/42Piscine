int	ft_sqrt(int nb)
{
	long	x;

	x = 1;
	if (nb == 1)
		return (nb);
	if (nb >= 2)
	{
		while (x * x <= nb)
		{
			if (x * x == nb)
				return (x);
			x++;
		}
	}
	return (0);
}
