#include <unistd.h>

int	ft_str_is_printable(char *str)
{
	int	i;

	i = 0;
	while (str[i] != '\0')
	{
		if (32 > str[i] || 126 < str[i])
			return (0);
		else
			i++;
	}
	return (1);
}
