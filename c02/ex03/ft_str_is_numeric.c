#include <unistd.h>

int	ft_str_is_numeric(char *str)
{
	int	i;

	i = 0;
	while (str[i] != '\0')
	{
		if (('0' <= str[i]) && ('9' >= str[i]))
			i++;
		else
			return (0);
	}
	return (1);
}
