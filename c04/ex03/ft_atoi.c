int	ft_atoi(char *str)
{
	int	i;
	int	m;
	int	s;
	int	pn;

	i = 0;
	s = 1;
	pn = 1;
	while ((str[i] == '-' || str[i] == '+')
		|| ((str[i] >= '\t' && str[i] <= '\r') || str[i] == ' '))
	{
		if (str[i] == '-')
			pn *= -1;
		i++;
	}
	while (str[i] >= '0' && str[i] <= '9')
		i++;
	m = 0;
	while (i--, str[i] >= '0' && str[i] <= '9')
	{
		m += ((str[i] - '0') * s);
		s *= 10;
	}
	return (m * pn);
}
